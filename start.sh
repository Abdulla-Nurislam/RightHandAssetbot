#!/bin/bash
# ─────────────────────────────────────────────────────────
# start.sh — Единственный правильный способ запуска бота.
# Автоматически убивает все старые экземпляры перед стартом.
# ─────────────────────────────────────────────────────────

echo "🔍 Останавливаю старые экземпляры бота..."
pkill -9 -f "python3 bot.py" 2>/dev/null
pkill -9 -f "python bot.py" 2>/dev/null
sleep 2

echo "🚀 Запускаю бот..."
python3 bot.py
